class RobertaLayer(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.chunk_size_feed_forward = config.chunk_size_feed_forward
        self.seq_len_dim = 1
        self.attention = RobertaAttention(config)
        self.is_decoder = config.is_decoder
        assert self.is_decoder == False
        self.add_cross_attention = config.add_cross_attention
        if self.add_cross_attention:
            if not self.is_decoder:
                raise ValueError(f'{self} should be used as a decoder model if cross attention is added')
            self.crossattention = RobertaAttention(config, position_embedding_type='absolute')
        self.intermediate = RobertaIntermediate(config)
        self.output = RobertaOutput(config)

    def feed_forward_chunk(self, attention_output):
        intermediate_output = self.intermediate(attention_output)
        layer_output = self.output(intermediate_output, attention_output)
        return layer_output

    def forward(
        self,
        hidden_states: torch.Tensor,
        attention_mask: Optional[torch.FloatTensor] = None,
        head_mask: Optional[torch.FloatTensor] = None,
        encoder_hidden_states: Optional[torch.FloatTensor] = None,
        encoder_attention_mask: Optional[torch.FloatTensor] = None,
        past_key_value: Optional[Tuple[Tuple[torch.FloatTensor]]] = None,
        output_attentions: Optional[bool] = False,
    ) -> Tuple[torch.Tensor]:
        # decoder uni-directional self-attention cached key/values tuple is at positions 1,2
        self_attn_past_key_value = past_key_value[:2] if past_key_value is not None else None
        self_attention_outputs = self.attention(
            hidden_states,
            attention_mask,
            head_mask,
            output_attentions=output_attentions,
            past_key_value=self_attn_past_key_value,
        )
        attention_output = self_attention_outputs[0]

        # if decoder, the last output is tuple of self-attn cache
        if self.is_decoder:
            outputs = self_attention_outputs[1:-1]
            present_key_value = self_attention_outputs[-1]
        else:
            outputs = self_attention_outputs[1:]  # add self attentions if we output attention weights

        cross_attn_present_key_value = None
        if self.is_decoder and encoder_hidden_states is not None:
            if not hasattr(self, 'crossattention'):
                raise ValueError(
                    f'If `encoder_hidden_states` are passed, {self} has to be instantiated with cross-attention layers'
                    ' by setting `config.add_cross_attention=True`'
                )

            # cross_attn cached key/values tuple is at positions 3,4 of past_key_value tuple
            cross_attn_past_key_value = past_key_value[-2:] if past_key_value is not None else None
            cross_attention_outputs = self.crossattention(
                attention_output,
                attention_mask,
                head_mask,
                encoder_hidden_states,
                encoder_attention_mask,
                cross_attn_past_key_value,
                output_attentions,
            )
            attention_output = cross_attention_outputs[0]
            outputs = outputs + cross_attention_outputs[1:-1]  # add cross attentions if we output attention weights

            # add cross-attn cache to positions 3,4 of present_key_value tuple
            cross_attn_present_key_value = cross_attention_outputs[-1]
            present_key_value = present_key_value + cross_attn_present_key_value

        layer_output = apply_chunking_to_forward(
            self.feed_forward_chunk,
            self.chunk_size_feed_forward,
            self.seq_len_dim,
            attention_output,
        )
        outputs = (layer_output,) + outputs

        # if decoder, return the attn key/values as the last output
        if self.is_decoder:
            assert False
            outputs = outputs + (present_key_value,)

        return outputs


class PositionalEncoding(nn.Module):
    def __init__(self, d_model, max_seq_len=6):
        super().__init__()

        # Compute the positional encoding once
        pos_enc = torch.zeros(max_seq_len, d_model)
        pos = torch.arange(0, max_seq_len, dtype=torch.float).unsqueeze(1)
        div_term = torch.exp(torch.arange(0, d_model, 2).float() * (-math.log(10000.0) / d_model))
        pos_enc[:, 0::2] = torch.sin(pos * div_term)
        pos_enc[:, 1::2] = torch.cos(pos * div_term)
        pos_enc = pos_enc.unsqueeze(0)

        # Register the positional encoding as a buffer to avoid it being
        # considered a parameter when saving the model
        self.register_buffer('pos_enc', pos_enc)

    def forward(self, x):
        # Add the positional encoding to the input
        x = x + self.pos_enc[:, : x.size(1), :]
        return x


class BertIntermediate(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.dense = nn.Linear(config.hidden_size, config.intermediate_size)
        if isinstance(config.hidden_act, str):
            self.intermediate_act_fn = ACT2FN[config.hidden_act]
        else:
            self.intermediate_act_fn = config.hidden_act

    def forward(self, hidden_states):
        hidden_states = self.dense(hidden_states)
        hidden_states = self.intermediate_act_fn(hidden_states)
        return hidden_states


class BertOutput(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.dense = nn.Linear(config.intermediate_size, config.hidden_size)
        self.LayerNorm = nn.LayerNorm(config.hidden_size, eps=config.layer_norm_eps)
        self.dropout = nn.Dropout(config.hidden_dropout_prob)

    def forward(self, hidden_states, input_tensor):
        hidden_states = self.dense(hidden_states)
        hidden_states = self.dropout(hidden_states)
        hidden_states = self.LayerNorm(hidden_states + input_tensor)
        return hidden_states


class BertCrossLayer(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.chunk_size_feed_forward = config.chunk_size_feed_forward
        self.seq_len_dim = 1
        self.attention = BertAttention(config)  # visn self
        self.is_decoder = config.is_decoder
        self.add_cross_attention = config.add_cross_attention
        self.crossattention = BertAttention(config)  # cross
        self.intermediate = BertIntermediate(config)
        self.output = BertOutput(config)

    def forward(
        self,
        hidden_states,
        encoder_hidden_states,
        attention_mask=None,
        encoder_attention_mask=None,
        output_attentions=False,
        graph_sprels=None,
        do_self_attn=True,
    ):
        # decoder uni-directional self-attention cached key/values tuple is at positions 1,2
        self_attn_past_key_value = None  # past_key_value[:2] if past_key_value is not None else None
        if graph_sprels is not None:
            attention_mask = attention_mask + graph_sprels
        if do_self_attn:
            self_attention_outputs = self.attention(
                hidden_states,
                attention_mask,
                head_mask=None,
                output_attentions=output_attentions,
                past_key_value=None,
            )
            attention_output = self_attention_outputs[0]
            # if decoder, the last output is tuple of self-attn cache
            outputs = self_attention_outputs[1:]  # add self attentions if we output attention weights
        else:
            attention_output = hidden_states
            outputs = ()

        cross_attn_present_key_value = None
        cross_attention_outputs = self.crossattention(
            attention_output,
            attention_mask,
            None,
            encoder_hidden_states,
            encoder_attention_mask,
            None,
            output_attentions,
        )
        attention_output = cross_attention_outputs[0]
        outputs = outputs + cross_attention_outputs[1:]  # add cross attentions if we output attention weights

        layer_output = apply_chunking_to_forward(
            self.feed_forward_chunk,
            self.chunk_size_feed_forward,
            self.seq_len_dim,
            attention_output,
        )
        outputs = (layer_output,) + outputs

        return outputs

    def feed_forward_chunk(self, attention_output):
        intermediate_output = self.intermediate(attention_output)
        layer_output = self.output(intermediate_output, attention_output)
        return layer_output

    def forward_lang2visn(
        self,
        lang_feats,
        lang_attention_mask,
        visn_feats,
        visn_attention_mask,
    ):
        cross_att_output = self.crossattention(
            lang_feats,
            lang_attention_mask,
            None,
            visn_feats,
            visn_attention_mask,
            None,
            output_attentions=False,
        )
        attention_output = cross_att_output[0]

        # self-attn & output
        lang_attn_output = self.lang_self_attn(
            attention_output,
            lang_attention_mask,
            head_mask=None,
            output_attentions=False,
            past_key_value=None,
        )[0]

        lang_inter_output = self.lang_inter(lang_attn_output)
        lang_output = self.lang_output(lang_inter_output, lang_attn_output)
        return lang_output


class CrossmodalEncoder(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.num_x_layers = config.num_x_layers
        self.crossattention = nn.ModuleList([BertCrossLayer(config) for _ in range(self.num_x_layers)])
        self.crossattention.apply(init_weights)

    def forward(
        self,
        q_embeds,
        q_masks,
        kv_embeds,
        kv_masks,
        graph_sprels=None,
        output_attentions=False,
        do_self_attn=True,
    ):
        if q_masks is not None and len(q_masks.shape) != 4:
            q_masks = extend_neg_masks(q_masks)
        if kv_masks is not None and len(kv_masks.shape) != 4:
            kv_masks = extend_neg_masks(kv_masks)
        for layer_module in self.crossattention:
            q_embeds = layer_module(
                hidden_states=q_embeds,
                encoder_hidden_states=kv_embeds,
                attention_mask=q_masks,
                encoder_attention_mask=kv_masks,
                graph_sprels=graph_sprels,
                output_attentions=output_attentions,
                do_self_attn=do_self_attn,
            )
            if type(q_embeds) == tuple:
                if len(q_embeds) == 3:
                    cross_attention_probs = q_embeds[2]
                    cross_attention_probs = nn.Softmax(dim=-1)(
                        torch.mean(cross_attention_probs, dim=1)
                    )  # compute the average values across multi-heads
                elif len(q_embeds) == 2 and (not do_self_attn):
                    cross_attention_probs = q_embeds[1]
                    cross_attention_probs = nn.Softmax(dim=-1)(torch.mean(cross_attention_probs, dim=1))
                else:
                    cross_attention_probs = None
                q_embeds = q_embeds[0]
        return q_embeds, cross_attention_probs
